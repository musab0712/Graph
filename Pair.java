public class Pair {
    int first;
    int second;
    int row;
    int col;
    int tm;
    public Pair(int first, int second) {
        this.first = first;
        this.second = second;
    }
    public Pair(int _row, int _col, int _tm) {
        this.row = _row;
        this.col = _col;
        this.tm = _tm;
    }
}
